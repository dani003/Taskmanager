     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request, Task $task)
    {
        //get all the task based on current user id
        $allTask = $task->whereIn('user_id', $request->user())->with('user');
        $tasks = $allTask->orderBy('created_at', 'desc')->take(20)->get();
        //return json respons
        return response()->json([
            'tasks' => $tasks,
        ]);
    }

